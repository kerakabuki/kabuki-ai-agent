/**
 * 翻訳関数
 * @param {string} key - 翻訳キー (例: "chat.title")
 * @param {string} lang - 言語コード ("ja" | "en")
 * @returns {string}
 */
export function t(key, lang = "ja") {
  const entry = translations[key];
  if (entry) return entry[lang] || entry["ja"] || key;
  const fallback = key.split(".").pop() || key;
  return fallback.replace(/_/g, " ");
}

/**
 * 言語別URLプレフィックス
 * @param {string} lang - 言語コード
 * @returns {string} "" (日本語) or "/en" (英語)
 */
export function langPrefix(lang = "ja") {
  return lang === "en" ? "/en" : "";
}
